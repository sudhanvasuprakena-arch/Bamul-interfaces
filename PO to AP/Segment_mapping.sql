-- ======================== RESOLUTION VIEW ========================
-- Use this view to resolve ITEM_ID → NEW_ACCOUNT_FLEX_VALUE as a fallback
-- when MAPPING_ACCOUNT_SEGMENT has no mapping for the old account.
--
-- Usage in PO/AP queries:
--   COALESCE(
--       mas.NEW_ACCOUNT_FLEX_VALUE,                        -- 1st: old account mapping
--       v_item.NEW_ACCOUNT_FLEX_VALUE,                     -- 2nd: item-based mapping
--       513001                                             -- 3rd: default General Consumables
--   )

CREATE OR REPLACE VIEW V_ITEM_ACCOUNT_FALLBACK AS
SELECT
    ps.INVENTORY_ITEM_ID,
    ppa.NEW_ACCOUNT_FLEX_VALUE,
    ppa.ACCOUNT_DESCRIPTION,
    'PRODUCT_PREFIX' AS MATCH_SOURCE
FROM MAPPING_PRODUCT_SEGMENT ps
JOIN MAPPING_PRODUCT_PREFIX_ACCOUNT ppa
  ON SUBSTR(ps.PRODUCT_CODE, 1, 2) = ppa.PRODUCT_PREFIX;


-- ---------------------------------------------------------------------------
-- Query 4A: MTL Accounting Lines vs Interface Log
--
-- Purpose  : Confirm all qualifying MTL_TRANSACTION_ACCOUNTS records for
--            the date range were processed or rejected.
-- When     : Run BEFORE Journal Import to verify the interface loaded correctly.
-- Expected : reconciliation_status = 'OK' for all rows.
-- ---------------------------------------------------------------------------
SELECT
    mmt.transaction_id,
    mmt.transaction_type_id,
    mmt.transaction_date,
    mta.reference_account     legacy_ccid,
    gcc.concatenated_segments legacy_account,
    mta.base_transaction_value,
    hou.name                  org_name,
    log.run_id,
    log.journal_category,
    log.period_name,
    log.new_ccid,
    log.interface_status,
    log.rejection_reason,
    CASE
        WHEN log.legacy_mtl_txn_id IS NULL THEN
            '** NOT INTERFACED — missing from log'
        WHEN log.interface_status = 'PROCESSED' THEN
            'OK — loaded to GL_INTERFACE'
        WHEN log.interface_status = 'REJECTED'  THEN
            'REJECTED: ' || log.rejection_reason
        WHEN log.interface_status = 'ERROR'     THEN
            'ERROR: ' || log.rejection_reason
        ELSE
            log.interface_status
    END                       reconciliation_status
FROM
         apps.mtl_material_transactions@legacy_instance mmt
    JOIN apps.mtl_transaction_accounts@legacy_instance mta ON mta.transaction_id = mmt.transaction_id
    LEFT JOIN apps.gl_code_combinations@legacy_instance     gcc ON gcc.code_combination_id = mta.reference_account
    LEFT JOIN apps.hr_organization_units@legacy_instance    hou ON hou.organization_id = mmt.organization_id
    LEFT JOIN xxcust_inv_gl_interface_log                   log ON log.legacy_mtl_txn_id = mmt.transaction_id
                                                 AND log.legacy_ccid = mta.reference_account
WHERE
    mmt.transaction_type_id IN ( 1, 2, 3, 4, 5,
                                 17, 21, 22, 24, 27,
                                 33, 34, 35, 44 )
    AND mta.base_transaction_value <> 0
    AND mmt.transaction_date BETWEEN :p_date_from AND :p_date_to
ORDER BY
    reconciliation_status,
    mmt.transaction_date,
    mmt.transaction_id,
    mta.reference_account;
-- ---------------------------------------------------------------------------
-- Query 4B: GL_INTERFACE vs GL_JE_LINES
--
-- Purpose  : Confirm Journal Import successfully created GL journal lines
--            for every row loaded to GL_INTERFACE.
-- When     : Run AFTER Journal Import program completes.
-- Expected : reconciliation_status = 'OK' for all rows.
-- Parameter: :p_group_id — from the interface run output log.
-- ---------------------------------------------------------------------------
SELECT
    gli.group_id,
    gli.user_je_category_name journal_category,
    gli.accounting_date,
    gli.period_name,
    gli.code_combination_id   new_ccid,
    gli.entered_dr,
    gli.entered_cr,
    gli.reference1            source_doc,
    gli.reference2            mtl_txn_id,
    gli.interface_status      gl_interface_status,
    jl.je_line_num            gl_je_line_num,
    jh.name                   gl_journal_name,
    jb.name                   gl_batch_name,
    jb.status                 batch_status,
    CASE
        WHEN jl.je_line_num IS NOT NULL THEN
            'OK — GL Journal created'
        WHEN gli.interface_status = 'E' THEN
            'GL IMPORT ERROR — check Journal Import execution report'
        ELSE
            'NOT IMPORTED — rerun Journal Import with Group ID=' || gli.group_id
    END                       reconciliation_status
FROM
    gl_interface  gli
    LEFT JOIN gl_je_lines   jl ON jl.code_combination_id = gli.code_combination_id
                                AND nvl(jl.entered_dr, 0) = nvl(gli.entered_dr, 0)
                                AND nvl(jl.entered_cr, 0) = nvl(gli.entered_cr, 0)
                                AND jl.reference_1 = gli.reference1
    LEFT JOIN gl_je_headers jh ON jh.je_header_id = jl.je_header_id
    LEFT JOIN gl_je_batches jb ON jb.je_batch_id = jh.je_batch_id
WHERE
        gli.user_je_source_name = 'XXCUST_INV_ACCT'
    AND gli.group_id = :p_group_id
ORDER BY
    reconciliation_status,
    gli.accounting_date,
    gli.user_je_category_name,
    gli.reference2;
-- ---------------------------------------------------------------------------
-- Query 4C: Journal Balance Check
--
-- Purpose  : Confirm total debits equal total credits for every journal
--            category within the group_id. Any non-zero variance must be
--            investigated before posting to the GL.
-- When     : Run AFTER loading GL_INTERFACE, BEFORE or AFTER Journal Import.
-- Expected : balance_status = 'BALANCED' for all rows.
--            variance = 0.00 for all rows.
-- ---------------------------------------------------------------------------
SELECT
    gli.group_id,
    gli.period_name,
    gli.user_je_category_name                                 journal_category,
    COUNT(*)                                                  line_count,
    SUM(nvl(gli.entered_dr, 0))                               total_dr,
    SUM(nvl(gli.entered_cr, 0))                               total_cr,
    SUM(nvl(gli.entered_dr, 0)) - SUM(nvl(gli.entered_cr, 0)) variance,
    CASE
        WHEN abs(sum(nvl(gli.entered_dr, 0)) - sum(nvl(gli.entered_cr, 0))) < 0.01 THEN
            'BALANCED'
        ELSE
            'OUT OF BALANCE -- INVESTIGATE BEFORE POSTING'
    END                                                       balance_status
FROM
    gl_interface gli
WHERE
        gli.user_je_source_name = 'XXCUST_INV_ACCT'
    AND gli.group_id = :p_group_id
GROUP BY
    gli.group_id,
    gli.period_name,
    gli.user_je_category_name
ORDER BY
    balance_status DESC,
    gli.period_name,
    gli.user_je_category_name;
-- ---------------------------------------------------------------------------
-- Query 4D: Inventory Valuation Amount Reconciliation
--
-- Purpose  : Confirm the total inventory accounting value loaded to the new
--            GL matches the total from legacy MTL_TRANSACTION_ACCOUNTS for
--            the same date range and organisation scope.
--            Compares debit-side totals (entered_dr).
-- When     : Run after GL_INTERFACE is loaded (before or after Journal Import).
-- Expected : Both source and target totals should match.
-- ---------------------------------------------------------------------------
SELECT
    'LEGACY: MTL_TRANSACTION_ACCOUNTS (debit lines)' data_source,
    COUNT(DISTINCT mmt.transaction_id)               transaction_count,
    SUM(mta.base_transaction_value)                  total_dr_amount
FROM
         apps.mtl_material_transactions@legacy_instance mmt
    JOIN apps.mtl_transaction_accounts@legacy_instance mta ON mta.transaction_id = mmt.transaction_id
WHERE
    mmt.transaction_type_id IN ( 1, 2, 3, 4, 5,
                                 17, 21, 22, 24, 27,
                                 33, 34, 35, 44 )
    AND mta.base_transaction_value > 0 -- Debit side only
    AND mmt.transaction_date BETWEEN :p_date_from AND :p_date_to
    AND ( p_organization_id IS NULL
          OR mmt.organization_id = :p_org_id )
UNION ALL
SELECT
    'NEW: GL_INTERFACE (entered_dr — debit lines)' data_source,
    COUNT(*)                                       transaction_count,
    SUM(nvl(gli.entered_dr, 0))                    total_dr_amount
FROM
    gl_interface gli
WHERE
        gli.user_je_source_name = 'XXCUST_INV_ACCT'
    AND gli.accounting_date BETWEEN :p_date_from AND :p_date_to
    AND gli.entered_dr IS NOT NULL;
-- ---------------------------------------------------------------------------
-- Query 4E: Interface Log Summary by Status and Journal Category
--
-- Purpose  : High-level summary of interface results for a given run.
-- When     : After each interface run.
-- ---------------------------------------------------------------------------
SELECT
    log.run_id,
    log.journal_category,
    log.interface_status,
    COUNT(*)                              line_count,
    COUNT(DISTINCT log.legacy_mtl_txn_id) transaction_count,
    SUM(nvl(log.entered_dr, 0))           total_dr,
    SUM(nvl(log.entered_cr, 0))           total_cr,
    MIN(log.accounting_date)              earliest_date,
    MAX(log.accounting_date)              latest_date
FROM
    xxcust_inv_gl_interface_log log
WHERE
    log.run_id = :p_run_id
GROUP BY
    log.run_id,
    log.journal_category,
    log.interface_status
ORDER BY
    log.interface_status,
    log.journal_category;
-- ---------------------------------------------------------------------------
-- Query 4F: Unmapped CCIDs — Pre-run check
--
-- Purpose  : Before running the interface, identify any legacy CCIDs
--            referenced by in-scope MTL_TRANSACTION_ACCOUNTS records that
--            are NOT yet in XXCUST_COA_MAPPING.
--            Run this query first. All rows should be resolved before
--            executing the interface.
-- When     : Before first interface run, and after any new accounts are
--            created in the legacy instance.
-- Expected : 0 rows returned.
-- ---------------------------------------------------------------------------
SELECT DISTINCT
    mta.reference_account                    legacy_ccid,
    gcc.concatenated_segments                legacy_account_num,
    mmt.transaction_type_id                  txn_type_id,
    hou.name                                 org_name,
    COUNT(mta.transaction_id)
    OVER(PARTITION BY mta.reference_account) affected_line_count
FROM
         apps.mtl_material_transactions@legacy_instance mmt
    JOIN apps.mtl_transaction_accounts@legacy_instance mta ON mta.transaction_id = mmt.transaction_id
    LEFT JOIN apps.gl_code_combinations@legacy_instance     gcc ON gcc.code_combination_id = mta.reference_account
    LEFT JOIN apps.hr_organization_units@legacy_instance    hou ON hou.organization_id = mmt.organization_id
WHERE
    mmt.transaction_type_id IN ( 1, 2, 3, 4, 5,
                                 17, 21, 22, 24, 27,
                                 33, 34, 35, 44 )
    AND mta.base_transaction_value <> 0
    AND mmt.transaction_date BETWEEN :p_date_from AND :p_date_to
    AND NOT EXISTS (
        SELECT
            1
        FROM
            xxcust_coa_mapping
        WHERE
                legacy_ccid = mta.reference_account
            AND mapping_status = 'ACTIVE'
            AND new_ccid IS NOT NULL
    )
ORDER BY
    mta.reference_account;

 SECTION 2: PACKAGE SPECIFICATION -- XXCUST_INV_GL_INTERFACE_PKG
 ============================================================================ */
CREATE OR REPLACE PACKAGE xxcust_inv_gl_interface_pkg AS
    /*
     || IF-03: Legacy Oracle Inventory --> New Oracle GL Journal Import Interface
     ||
     || Extracts MTL_TRANSACTION_ACCOUNTS entries for in-scope inventory
     || transaction types from the legacy instance via DB link and loads
     || them into GL_INTERFACE in the new instance.
     ||
     || Transaction types covered:
     ||   PO Receipts (1, 17, 27), Transfers (2, 3, 21, 22),
     ||   SO Issues (33, 34), Adjustments (4, 5, 24),
     ||   WIP (35, 44)
     ||
     || Call run_interface() then run standard Journal Import in new GL:
     ||   Source   : XXCUST_INV_ACCT
     ||   Group ID : returned in p_group_id OUT parameter
     */
    -- -------------------------------------------------------------------------
    -- Main interface procedure.
    -- Extracts inventory accounting lines and loads to GL_INTERFACE.
    -- Returns the group_id to use when running Journal Import.
    -- -------------------------------------------------------------------------
    PROCEDURE run_interface (
        p_errbuf          OUT VARCHAR2,
        p_retcode         OUT NUMBER,
        p_group_id        OUT NUMBER,
        -- group_id for Journal Import
        p_organization_id IN NUMBER DEFAULT NULL,
        -- NULL = all orgs
        p_txn_date_from   IN VARCHAR2 DEFAULT NULL,
        -- DD-MON-YYYY
        p_txn_date_to     IN VARCHAR2 DEFAULT NULL,
        -- DD-MON-YYYY
        p_txn_type_ids    IN VARCHAR2 DEFAULT NULL,
        -- CSV list e.g. '1,17,33'
        -- NULL = all in-scope types
        p_debug_mode      IN VARCHAR2 DEFAULT 'N'
    );
-- -------------------------------------------------------------------------
-- Purge processed log records older than N days.
-- -------------------------------------------------------------------------
    PROCEDURE purge_log (
        p_days_to_keep IN NUMBER DEFAULT 90
    );

END xxcust_inv_gl_interface_pkg;

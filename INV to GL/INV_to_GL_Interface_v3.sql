-- Helper function to determine transaction type segment for tax code
-----------------------------------------------------------------------------
    FUNCTION get_txn_type_segment (
        p_rcv_transaction_id IN NUMBER
    ) RETURN VARCHAR2 IS
        l_tax_code VARCHAR2(100);
    BEGIN -- Get the first tax_rate_code from JAI_TAX_LINES_ALL for this receipt transaction
        SELECT
            lower(jtl.tax_rate_code)
        INTO l_tax_code
        FROM
            apps.jai_tax_lines_all@legacy_instance jtl
        WHERE
                jtl.trx_id = p_rcv_transaction_id
            AND ROWNUM = 1;

        IF l_tax_code LIKE 'cgst%'
           OR l_tax_code LIKE 'sgst%' THEN
            RETURN '1';
-- LOCAL SALE
        ELSIF l_tax_code LIKE 'igst%' THEN
            RETURN '2';
-- INTERSTATE
        ELSE
            RETURN '0';
-- DEFAULT
        END IF;

    EXCEPTION
        WHEN no_data_found THEN
            RETURN '0';
-- DEFAULT (no tax lines found)
        WHEN OTHERS THEN
            RETURN '0';
    END get_txn_type_segment;

/* ============================================================================
 FILE:         INV_to_GL_Interface.sql
 INTERFACE:    IF-03: Legacy Oracle INV (Inventory / Cost Management)
 --> New Oracle GL (Journal Import)
 VERSION:      1.0
 PLATFORM:     Oracle EBS R12.2 (Legacy) --> Oracle EBS R12.2 (New Instance)
 
 DESCRIPTION:
 ------------
 Extracts inventory accounting entries generated by material transactions
 in the legacy Oracle Inventory and Cost Management modules and loads them
 into the new Oracle GL via the GL_INTERFACE open interface table.
 
 The standard Oracle Journal Import program (Source: XXCUST_INV_ACCT)
 completes journal creation in the new GL instance after each interface run.
 
 CORE BUSINESS RULE:
 Only material transactions that have generated accounting entries in
 MTL_TRANSACTION_ACCOUNTS AND whose accounting_date falls within an open
 or future-enterable GL period in the new instance will be interfaced.
 
 IN-SCOPE TRANSACTION TYPES (MTL_MATERIAL_TRANSACTIONS.transaction_type_id):
 ─────────────────────────────────────────────────────────────────────────────
 1   Purchase Order Receipt (into Receiving Inspection)
 2   Subinventory Transfer
 3   Direct Inter-Organisation Transfer
 4   Cycle Count Adjustment / Physical Inventory Adjustment / Misc Adjustment
 5   Miscellaneous Issue / Receipt
 17  PO Deliver to Subinventory  /  RMA Receipt to Stock
 21  Intransit Shipment (Inter-Org)
 22  Intransit Receipt (Inter-Org)
 24  Standard Cost Update
 27  Receipt into Inspection (Return PO)
 33  Sales Order Issue (COGS)
 34  Account Alias Issue
 35  WIP Component Issue
 44  WIP Assembly Completion
 
 JOURNAL CATEGORIES (registered in new GL, source: XXCUST_INV_ACCT):
 ─────────────────────────────────────────────────────────────────────────────
 Receiving           -- Types 1, 17 (PO receipt stages)
 Inventory           -- Types 2, 3, 21, 22
 Cost of Goods Sold  -- Types 33 (Sales Order Issues)
 Inventory Accounting-- Types 4, 5, 24, 27, 34 (Adjustments, Misc, Std Cost)
 WIP                 -- Types 35, 44
 
 SOURCE TABLES (LEGACY INSTANCE -- accessed via DB Link: LEGACY_INSTANCE):
 ─────────────────────────────────────────────────────────────────────────────
 MTL_MATERIAL_TRANSACTIONS      Transaction header (type, date, org, item)
 MTL_TRANSACTION_ACCOUNTS       Accounting lines (CCID, signed amount)
 MTL_SYSTEM_ITEMS_B             Item master (description, UOM)
 MTL_PARAMETERS                 Org costing method
 CST_COST_GROUPS                Cost group master (Average Cost orgs)
 GL_CODE_COMBINATIONS           Legacy COA (concatenated segment string)
 GL_PERIODS                     Legacy GL period calendar
 HR_ORGANIZATION_UNITS          Organisation name
 PO_HEADERS_ALL                 PO number (for receipt transactions)
 OE_ORDER_HEADERS_ALL           Sales Order number (for SO issue transactions)
 WIP_DISCRETE_JOBS              WIP job number (for WIP transactions)
 
 TARGET TABLES (NEW INSTANCE):
 ─────────────────────────────────────────────────────────────────────────────
 GL_INTERFACE                   Standard Oracle GL open interface table
 GL_JE_BATCHES                  Queried post-import (confirmation)
 GL_JE_HEADERS                  Queried post-import (confirmation)
 GL_JE_LINES                    Queried post-import (reconciliation)
 XXCUST_INV_GL_INTERFACE_LOG    Custom control and audit log
 XXCUST_COA_MAPPING             Legacy CCID --> New CCID mapping (shared)
 
 PREREQUISITES:
 ─────────────────────────────────────────────────────────────────────────────
 1.  DB link LEGACY_INSTANCE created with SELECT grants on all source tables
 2.  GL source XXCUST_INV_ACCT registered in new GL
 3.  Journal categories registered: Receiving, Inventory, Cost of Goods Sold,
 Inventory Accounting, WIP
 4.  XXCUST_COA_MAPPING populated for all inventory account CCIDs
 (Inventory Valuation, Receiving Inspection, AP Accrual, COGS, Intransit,
 WIP Valuation, Adjustment/Variance accounts for all orgs in scope)
 5.  GL period(s) open in new instance for the transaction date range
 6.  Best practice: inventory accounting period closed in legacy instance
 before running to capture all cost adjustments for the period
 7.  XXCUST_INV_GL_INTERFACE_LOG DDL executed (Section 1)
 8.  XXCUST_INV_GL_INTERFACE_PKG compiled (Sections 2-3)
 
 TABLE OF CONTENTS:
 ─────────────────────────────────────────────────────────────────────────────
 SECTION 1  : DDL -- XXCUST_INV_GL_INTERFACE_LOG (Control / Audit Log)
 SECTION 2  : Package Specification -- XXCUST_INV_GL_INTERFACE_PKG
 SECTION 3  : Package Body
 3A: Private utilities (log_msg, get_new_ccid, get_period_name,
 get_sob_id, get_je_category, get_source_doc_ref,
 validate_transaction, load_gl_line)
 3B: run_interface -- main processing procedure
 Cursor: c_inv_accounting (MTL_TRANSACTION_ACCOUNTS)
 Processing loop with E-V-T-L pattern
 3C: purge_log
 SECTION 4  : Reconciliation Queries
 4A: MTL accounting lines vs interface log
 4B: GL_INTERFACE vs GL_JE_LINES (post-import)
 4C: Journal balance check (DR = CR per group_id)
 4D: Inventory valuation amount reconciliation
 SECTION 5  : Execution Guide
 
 CHANGE LOG:
 ─────────────────────────────────────────────────────────────────────────────
 v1.0   Initial release
 ============================================================================ */

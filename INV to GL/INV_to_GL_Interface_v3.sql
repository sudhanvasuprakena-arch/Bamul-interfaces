-------------------------------------------------------------------------
-- Fallback: Derive account segment from item when old account has no mapping.
-- Resolution: 1) Product prefix → account  2) Item description keywords → account
--             3) Default 513001 (General Consumables)
-------------------------------------------------------------------------
    FUNCTION get_account_segment_fallback (
        p_inventory_item_id IN NUMBER
    ) RETURN VARCHAR2 IS

        l_new_segment    VARCHAR2(6);
        l_product_code   VARCHAR2(8);
        l_product_prefix VARCHAR2(4);
        l_item_desc      VARCHAR2(240);
    BEGIN
        log_msg('Account fallback: starting for item_id=' || p_inventory_item_id);
-- Step 1: Try product prefix mapping (item already in MAPPING_PRODUCT_SEGMENT)
        BEGIN
            SELECT
                ps.product_code
            INTO l_product_code
            FROM
                apps.mapping_product_segment ps
            WHERE
                ps.inventory_item_id = p_inventory_item_id;

            l_product_prefix := substr(l_product_code, 1, 2);
            log_msg('Account fallback Step 1: item_id='
                    || p_inventory_item_id
                    || ' → product_code='
                    || l_product_code
                    || ' → prefix='
                    || l_product_prefix);

            SELECT
                to_char(ppa.new_account_flex_value)
            INTO l_new_segment
            FROM
                apps.mapping_product_prefix_account ppa
            WHERE
                ppa.product_prefix = l_product_prefix;

            log_msg('Account fallback Step 1: prefix='
                    || l_product_prefix
                    || ' → account='
                    || l_new_segment);
            RETURN l_new_segment;
        EXCEPTION
            WHEN no_data_found THEN
                log_msg('Account fallback Step 1: no mapping found, falling through to keyword matching');
                NULL;
-- fall through to keyword matching
        END;
-- Step 2: Try keyword matching on item description
        BEGIN
            SELECT
                description
            INTO l_item_desc
            FROM
                apps.mtl_system_items_b@legacy_instance
            WHERE
                    inventory_item_id = p_inventory_item_id
                AND ROWNUM = 1;

            log_msg('Account fallback Step 2: item_id='
                    || p_inventory_item_id
                    || ' → description='
                    || substr(l_item_desc, 1, 100));

        EXCEPTION
            WHEN no_data_found THEN
                log_msg('Account fallback Step 2: no item description found, returning default 513001');
                RETURN '513001';
-- default
        END;

        BEGIN
            SELECT
                to_char(ka.new_account_flex_value)
            INTO l_new_segment
            FROM
                (
                    SELECT
                        ka.new_account_flex_value
                    FROM
                        apps.mapping_item_keyword_account ka
                    WHERE
                        upper(l_item_desc) LIKE upper(ka.keyword_pattern)
                    ORDER BY
                        ka.priority ASC
                ) ka
            WHERE
                ROWNUM = 1;

            log_msg('Account fallback Step 2: keyword match → account=' || l_new_segment);
            RETURN l_new_segment;
        EXCEPTION
            WHEN no_data_found THEN
                log_msg('Account fallback Step 2: no keyword match, returning default 513001');
                RETURN '513001';
-- default: General Consumables
        END;

    EXCEPTION
        WHEN OTHERS THEN
            log_msg('Account fallback: unexpected error: '
                    || sqlerrm
                    || ', returning default 513001');
            RETURN '513001';
    END get_account_segment_fallback;
